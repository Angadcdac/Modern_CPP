#include <iostream>  // Standard Input/Output Stream Library
using namespace std; // Use names from the standard namespace allow cin cout
int main(void)       // Entry point of the program
{
    /*
     * cout  -> Standard output stream object
     * <<    -> Stream insertion operator
     * endl  -> Inserts a newline and flushes the output stream
     */
    cout << "Hello Angad welcome in cpp programming" << endl;

    /*
     * return 0;
     * Returns control to the operating system.
     * 0 indicates that the program executed successfully.
     */
    return 0;
}


